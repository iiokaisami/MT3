Vector3 Project(const Vector3& v1, const Vector3& v2)
{
	Vector3 result;
	float t = Dot(v1, v2) / (sqrtf(Dot(v2,v2))* sqrtf(Dot(v2, v2)));

	result = Multiply(t, v2);
	
	return result;
}

